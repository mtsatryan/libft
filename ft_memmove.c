#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char *s;
	char *d;

	s = (char *)src;
	d = (char *)dst;
	if (d + len < s || s + len < d)
		while (len--)
			*d++ = *s++;
	else
	{
		d = d + len - 1;
		s = s + len - 1;
		while (len--)
			*d-- = *s--;
	}
	return (d);
}
